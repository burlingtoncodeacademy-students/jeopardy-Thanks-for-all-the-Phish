let createDiv = (id, value, question, answer) => {
  let div = document.createElement("div");
  div.innerHTML = `
    <div id="${id}" class="item">
        <p class="value">${value}</p>
        <p class="question">${question}</p>
        <p class="answer">${answer}</p>
    </div>
    `;
  grid.appendChild(div);
};

