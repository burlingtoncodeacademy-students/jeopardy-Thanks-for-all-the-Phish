let selectDiv = (id) => {
  let div = document.querySelector(`#${id}`);
  div.classList.toggle("selected");
};

allItems.forEach((item) => {
  item.addEventListener("click", (event) => {
    // if click target is actually a <p> element inside the <div>
    if (
      event.target.classList.contains("value") ||
      event.target.classList.contains("question")
    ) {
      
      let div = event.target.parentNode;
      selectDiv(div.id);
    } else {
      
      // if click target is in fact the div
      selectDiv(event.target.id);
    
    }
  });
});

